#return = statement used to end a function
#         and send a result back to the caller

def add(x,y):
    z=x+y
    return z

def sub(x,y):
    z=x-y
    return z 

def mul(x,y):
    z=x*y
    return z 

def divide(x,y):
    z=x/y
    return z 


print(add(1,2))
print(sub(1,2))
print(mul(1,2))
print(divide(1,2))

