import math #math module imported

x=9.1
y=9.9

print(math.pi)  #prints 3.141592653589793

print(math.e) #prints 2.718281828459045

print(math.sqrt(x)) #prints 3

result = math.ceil(x)  #ceil will always round the value to up like for 9.1 to 10
print(result)

result = math.floor(y)  #ceil will always round the value to up like for 9.9 to 9
print(result)
