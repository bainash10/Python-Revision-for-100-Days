# Types of data or datatypes in python

#Integer
age = 12
players = 8
quantity = 5

print(f"You are {age} years old")
print(f"There are {players} players online")
print(f"You would like to buy {quantity} items")
print("\n") #new line

#Float
gpa=3.54
distance = 2.4
price=5.98

print(f"Your GPA is {gpa}")
print(f"Your ran {distance}km")
print(f"The price is ${distance}")


#String
name="Nischal"
food="Pizza"
email="nsichal@gmail.com"

print(f"Hello {name}")
print(f"You like {food}")
print(f"Your email is {email}")

print("\n")
#Boolean
#Booleans should not be kept inside quotes and remember first letter is always Capital like: T or F
online = True 
for_sale = False

print(f"Are you online?: {online}")
print(f"Is this for sale?: {for_sale}")